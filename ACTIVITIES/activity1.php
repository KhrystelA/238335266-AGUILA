<?php
// Activity 1: Number Counter
// Printing numbers from 1 to 10 using a while loop
$count = 1;
while ($count <= 10) {
    echo $count . " ";
    $count++;
}

// Printing even numbers between 1 and 20
echo "<br>Even numbers between 1 and 20:<br>";
$count = 2;
while ($count <= 20) {
    echo $count . " ";
    $count += 2;
}
?>
